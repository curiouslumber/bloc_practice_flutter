class AccountUser {
  String name = "user";
  String email = "email";

  void setAccountName(String name) {
    this.name = name;
  }

  void setAccountEmail(String email) {
    this.email = email;
  }

  String getAccountName() {
    return name;
  }

  String getAccountEmail() {
    return email;
  }
}
